package model;

/**
 *
 * @author Geral
 */
public class ClienteModel {
    
    private int id;
    private String nome;
    private String endereco;
    private String fone;
    private String email;

    public ClienteModel() {
    }

    public ClienteModel(int id, String nome, String endereco, String fone, String email) {
        this.id = id;
        this.nome = nome;
        this.endereco = endereco;
        this.fone = fone;
        this.email = email;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getEndereco() {
        return endereco;
    }

    public void setEndereco(String endereco) {
        this.endereco = endereco;
    }

    public String getFone() {
        return fone;
    }

    public void setFone(String fone) {
        this.fone = fone;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }
    
    
    
    
}
